# Método Python String title()
# Faça a primeira letra em cada palavra maiúscula.
# Se a palavra contiver um número ou um símbolo, a primeira letra depois disso ser convertido para maiúsula.
texto = 'welcome to my word'
print(texto.title())

# Exemplo com número
texto2 = 'welcome to my 2nd world'
print(texto2.title())

# Observe que a primeira letra após uma letra não-alfabeto é convertida em um letra maiúscula
texto3 = 'hello b2b2b2 and 3g3g3g'
print(texto3.title())

