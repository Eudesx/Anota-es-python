# Python String capitalize() Method
# Transforma para maiúscula a primeira letra de uma frase
texto = 'hello, and welcome to my word.'
print(texto.capitalize())

# O primeiro caractere é convertido em maiúscula, e o resto é convertido em minúsculas:
texto2 = 'python is FUN'
print(texto2.capitalize())

# Veja o que acontece se o primeiro caractere for um número:
texto3 = '36 is my age.'
print(texto3.capitalize())
