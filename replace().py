# Método de substituição replace()

texto = 'I like bananas'
print(texto.replace('bananas', 'maçã'))

# Substitua toda a ocorrência da palavra "um":

txt = '"um era um cavalo de corrida, dois dois também era um."'
print(txt.replace('um', 'Três'))

# Substitua a primeira ocorrência da palavra "um":

texto2 = 'um era um cavalo de corrida, dois dois também era um.'
print(texto2.replace('um', 'três', 1))

