# Método Python String find()

txt = 'Olá, Bem-vindo ao meu mundo'
print(txt.find('mundo'))

# Onde no texto é a primeira ocorrência da letra "e"?:

texto = 'Hello, welcome to my word.'
print(texto.find('e'))

# Onde no texto é a primeira ocorrência da letra "e" quando Você só procura entre as posições 5 e 10?:

texto2 = 'Hello, welcome to my word.'
print(texto2.find('e', 5, 10))

